    private void configurarBanco() {
        
        switch (cbSgbd.getSelectedItem().toString()) {
            case "PostgreSQL":
                txtDriver.setText("org.postgresql.Driver");
                txtBanco.setText("jdbc:postgresql://localhost/teste"); //mudar aqui
                txtUsuario.setText("teste"); //mudar aqui
                pfSenha.setText("teste"); //mudar aqui
                break;
            case "Oracle":
                txtDriver.setText("oracle.jdbc.OracleDriver");
                txtBanco.setText("jdbc:oracle:thin:@localhost:1521:XE");
                txtUsuario.setText("unip"); //mudar aqui
                pfSenha.setText("unip"); // mudar aqui
                break;
            case "MySQL":
                txtDriver.setText("com.mysql.jdbc.Driver");
                txtBanco.setText("jdbc:mysql://localhost:3306/test"); //mudar aqui
                txtUsuario.setText("root"); //mudar aqui
                pfSenha.setText("root"); //mudar aqui
                break;
            default:
                JOptionPane.showMessageDialog(null,
                    "Banco de dados não possui driver JDBC instalado\n"
                    + "Confira no arquivo pom.xml");
        }        
    }

