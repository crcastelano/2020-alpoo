    private void btnExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExecutarActionPerformed
        String driver = txtDriver.getText();
        String banco = txtBanco.getText();
        String usuario = txtUsuario.getText();
        String senha = pfSenha.getText();
        String sql = taSql.getText();

        crudJDBC.conectar(driver, banco, usuario, senha);
        
        switch (operacao) {
            case "consultar":
                taResultado.setText( crudJDBC.consultar( sql ) );
                break;
            case "incluir":
                crudJDBC.incluir( sql );
                break;
            case "alterar":
                crudJDBC.alterar( sql );
                break;
            case "excluir":
                crudJDBC.excluir( sql );
                break;
            default:
                JOptionPane.showMessageDialog(null, "Operação Inválida: "+ operacao);
                
        }
    }//GEN-LAST:event_btnExecutarActionPerformed

